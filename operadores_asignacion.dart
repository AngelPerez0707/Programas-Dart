void main() {
  double edad = 24;
  edad += 1; //edad = edad + 1
  print("Después de sumar 1,la edad es $edad");

  edad -= 1; //edad = edad - 1
  print("Después de restar 1,la edad es $edad");

  edad *= 2; //edad = edad * 2
  print("Después de multiplicar por 2,la edad es $edad");

  edad /= 2; //edad = edad / 2
  print("Después de dividir entre 2,la edad es $edad");
}
